package com.example.primeiro

fun soma() {
    val num1: Int = 12
    val num2: Int = 4
    val resultado: Int = num1 + num2

    println("Soma: $resultado")
}

fun subtracao() {
    val num1: Int = 12
    val num2: Int = 4
    val resultado: Int = num1 - num2

    println("Subtração: $resultado")
}

fun multiplicacao() {
    val num1: Int = 12
    val num2: Int = 4
    val resultado: Int = num1 * num2

    println("Multiplicação: $resultado")
}

fun divisao() {
    val num1: Int = 12
    val num2: Int = 4
    val resultado: Int = num1 / num2

    println("Divisão: $resultado")
}

fun main () {
    soma()
    subtracao()
    multiplicacao()
    divisao()
}